"""LLM-judge evaluation rule."""

import base64
from typing import Literal, TYPE_CHECKING

from pydantic import Field
from openai.types.chat import (
    ChatCompletionContentPartImageParam,
    ChatCompletionContentPartParam,
    ChatCompletionContentPartTextParam,
    ChatCompletionMessageParam,
    ChatCompletionSystemMessageParam,
    ChatCompletionUserMessageParam,
)

from h_arcane.core.evaluation.rules.base import BaseRule
from h_arcane.core.db.models import CriterionResult, Resource

if TYPE_CHECKING:
    from h_arcane.core.evaluation.context import EvaluationRunner


class LLMJudgeRule(BaseRule):
    """LLM-judge evaluation rule generated by LLM.

    Uses another LLM call to evaluate the criterion qualitatively.
    """

    type: Literal["llm_judge"] = "llm_judge"
    judge_prompt: str = Field(
        description="Prompt template for the judge LLM to evaluate this criterion",
    )
    expectation: str | None = Field(
        default=None,
        description="Optional guidance describing ideal outcome",
    )
    # Extended fields for ResearchRubrics
    axis: str | None = Field(
        default=None,
        description="Axis type for analysis (Implicit/Explicit/Synthesis/etc.)",
    )

    async def evaluate(self, runner: "EvaluationRunner") -> CriterionResult:
        """Evaluate using LLM judge with granular Inngest steps."""
        data = runner.data

        # Build messages (no step needed - just object construction)
        text_content = self._build_text_content(
            data.task_input, data.agent_reasoning, data.max_score
        )
        content_parts = self._build_content_parts(text_content, data.agent_outputs)
        messages = self._build_messages(content_parts)

        # Store messages as string for debugging
        evaluation_input = str(messages)

        # Step 1: Call LLM API with structured output
        async def call_llm_api():
            # Import here to avoid circular dependency
            from h_arcane.core.evaluation.schemas import LLMJudgeResponse

            response = await runner.call_llm_judge(messages, LLMJudgeResponse)
            # Return serializable dict for Inngest step
            return {
                "reasoning": response.reasoning,
                "final_verdict": response.final_verdict,
            }

        try:
            llm_result = await runner.step("call-llm-api", call_llm_api)

            # Step 2: Convert verdict to score (simple, but as step for visibility)
            async def compute_score():
                score = data.max_score if llm_result["final_verdict"] else 0.0
                return {"score": score, "feedback": llm_result["reasoning"]}

            score_result = await runner.step("compute-score", compute_score)
            score = score_result["score"]
            feedback = score_result["feedback"]

        except Exception as e:
            # Handle errors gracefully
            feedback = f"Error in LLM judge evaluation: {e}"
            score = 0.0

        # Build and return criterion result
        return CriterionResult(
            run_id=data.run_id,
            stage_num=data.stage_idx,
            stage_name=data.stage_name,
            criterion_num=data.rule_idx,
            criterion_type="llm_judge",
            criterion_description=self.description,
            score=score,
            max_score=data.max_score,
            feedback=feedback,
            evaluation_input=evaluation_input,
            evaluated_action_ids=[],
            evaluated_resource_ids=[str(r.id) for r in data.agent_outputs],
        )

    def _build_text_content(
        self,
        task_input: str,
        agent_reasoning: str,
        max_score: float,
    ) -> str:
        """Build text content for LLM judge evaluation prompt."""
        expectation_text = f"\nExpectation: {self.expectation}" if self.expectation else ""
        return f"""Task Input: {task_input}

Agent Reasoning/Output: {agent_reasoning}

Criterion: {self.description}{expectation_text}

Evaluate whether the output meets this criterion. Provide:
1. Detailed reasoning explaining your decision, citing specific evidence from the task input, agent reasoning, and outputs
2. A binary verdict: True if the criterion is met, False otherwise

This is a pass/fail decision. The criterion is either satisfied (True) or not satisfied (False).
"""

    def _build_content_parts(
        self,
        text_content: str,
        agent_outputs: list[Resource],
    ) -> list[ChatCompletionContentPartParam]:
        """Build content parts for LLM judge evaluation with multimodal support."""
        content_parts: list[ChatCompletionContentPartParam] = [
            ChatCompletionContentPartTextParam(
                type="text",
                text=text_content,
            )
        ]

        # Add file content as images (for PDFs, Excel, images)
        for resource in agent_outputs:
            if resource.mime_type.startswith("image/"):
                content_bytes = resource.load_content()
                base64_content = base64.b64encode(content_bytes).decode()
                content_parts.append(
                    ChatCompletionContentPartImageParam(
                        type="image_url",
                        image_url={"url": f"data:{resource.mime_type};base64,{base64_content}"},
                    )
                )
            elif resource.mime_type in [
                "application/pdf",
                "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
                "application/vnd.openxmlformats-officedocument.wordprocessingml.document",
            ]:
                # For PDFs and Office docs, include text preview in the prompt
                try:
                    preview = resource.preview_text or resource.load_text()[:500]
                    if content_parts:
                        first_part = content_parts[0]
                        # Access text field from TypedDict
                        if isinstance(first_part, dict) and first_part.get("type") == "text":
                            existing_text_value = first_part.get("text")
                            existing_text = (
                                existing_text_value if isinstance(existing_text_value, str) else ""
                            )
                            content_parts[0] = ChatCompletionContentPartTextParam(
                                type="text",
                                text=existing_text
                                + f"\n\nFile: {resource.name}\nPreview: {preview}...",
                            )
                except Exception:
                    if content_parts:
                        first_part = content_parts[0]
                        if isinstance(first_part, dict) and first_part.get("type") == "text":
                            existing_text_value = first_part.get("text")
                            existing_text = (
                                existing_text_value if isinstance(existing_text_value, str) else ""
                            )
                            content_parts[0] = ChatCompletionContentPartTextParam(
                                type="text",
                                text=existing_text + f"\n\nFile: {resource.name} (binary file)",
                            )

        return content_parts

    def _build_messages(
        self,
        content_parts: list[ChatCompletionContentPartParam],
    ) -> list[ChatCompletionMessageParam]:
        """Build messages list for LLM judge evaluation."""
        return [
            ChatCompletionSystemMessageParam(
                role="system",
                content=self.judge_prompt,
            ),
            ChatCompletionUserMessageParam(
                role="user",
                content=content_parts,
            ),
        ]
